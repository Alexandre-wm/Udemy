'''
 Herança simples - Relações entre classes

 Associação: Indica que um objeto está relacionado a 
outro objeto, mas não há uma propriedade de 
propriedade. Pode ser um relacionamento de um para um, 
um para muitos, muitos para muitos, etc.

 Agregação: Indica que um objeto "tem" outro objeto 
como parte de sua composição. 
A diferença chave com a composição é que, 
na agregação, os objetos podem existir 
independentemente uns dos outros.

 Composição: Indica uma relação mais forte do que a 
agregação, onde um objeto é parte integrante de outro 
objeto. Se o objeto pai for destruído, os objetos 
filhos também serão.

 Herança: Permite que uma classe (objeto) herde 
atributos e métodos de outra classe (objeto). 
É uma forma de reutilização de código e estabelece 
uma relação "é um".

Herança vs Composição 

Classe principal (Pessoa)
    -> super class, base class, parent class
    
Classes filhas (cliente)
    -> sub class, child class, derived class
'''

